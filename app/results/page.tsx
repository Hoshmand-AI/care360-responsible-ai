'use client'

import { useEffect, useState } from 'react'
import { useRouter } from 'next/navigation'
import Link from 'next/link'
import { CheckCircle, AlertTriangle, AlertCircle, Phone, MapPin, MessageCircle, ChevronDown, ChevronUp, Shield, ExternalLink } from 'lucide-react'
import { FeedbackWidget } from '@/components/responsible/FeedbackWidget'

interface OTCSuggestion {
  name: string; brands: string[]; purpose: string
  source?: string; contraindications?: string[]; warnings?: string[]
}
interface SymptomResults {
  input: { symptoms: string[]; duration?: string; severity?: string }
  results: {
    urgencyLevel: 'routine' | 'soon' | 'urgent' | 'emergency'
    confidenceScore?: number
    possibleCauses: string[]
    recommendations: string[]
    explanationOfReasoning?: string
    otcSuggestions: OTCSuggestion[]
    warningSignsToWatch: string[]
    disclaimers: string[]
  }
}

export default function ResultsPage() {
  const router = useRouter()
  const [data, setData] = useState<SymptomResults | null>(null)
  const [mounted, setMounted] = useState(false)
  const [showExplanation, setShowExplanation] = useState(false)

  useEffect(() => {
    setMounted(true)
    const stored = sessionStorage.getItem('symptomResults')
    if (!stored) { router.replace('/symptoms'); return }
    try {
      const parsed = JSON.parse(stored)
      if (!parsed?.results?.urgencyLevel || !parsed?.input?.symptoms?.length) { router.replace('/symptoms'); return }
      setData(parsed)
    } catch { router.replace('/symptoms') }
  }, [router])

  if (!mounted || !data) return (
    <div className="min-h-screen bg-slate-50 flex items-center justify-center">
      <div className="text-center"><div className="w-8 h-8 border-2 border-teal-500 border-t-transparent rounded-full animate-spin mx-auto mb-3" /><p className="text-slate-400">Loading results...</p></div>
    </div>
  )

  const { input, results } = data
  const urgencyConfig = getUrgencyConfig(results.urgencyLevel)
  const confidence = results.confidenceScore ?? 75

  return (
    <div className="min-h-screen bg-slate-50">
      <div className="max-w-2xl mx-auto px-4 sm:px-6 py-8 sm:py-12">
        <h1 className="text-2xl sm:text-3xl font-display text-navy-950 mb-2">Your Results</h1>
        <p className="text-slate-500 mb-2">Based on: {input.symptoms.join(', ')}</p>

        {/* v2: RAI badge */}
        <div className="inline-flex items-center gap-1.5 bg-teal-50 border border-teal-200 text-teal-700 text-xs font-medium px-3 py-1 rounded-full mb-6">
          <Shield className="w-3 h-3" /> Responsible AI Edition · GPT-4o · Sources cited · Human review enabled
        </div>

        {/* v2: AI Disclosure */}
        <div className="bg-teal-50 border border-teal-200 rounded-lg px-4 py-3 flex items-start gap-3 text-sm mb-4">
          <span className="text-teal-600 text-lg flex-shrink-0">🤖</span>
          <p className="text-teal-700"><strong>AI Disclosure:</strong> Analysis generated by <strong>GPT-4o (OpenAI)</strong>. Results are general wellness guidance only — not a medical diagnosis.</p>
        </div>

        {/* v2: Confidence Score — Roxette */}
        <div className="bg-white border border-slate-200 rounded-lg p-5 mb-5">
          <div className="flex items-center justify-between mb-2">
            <p className="text-sm font-semibold text-slate-700">AI Confidence Score</p>
            <span className={`text-lg font-bold ${confidence >= 70 ? 'text-teal-600' : confidence >= 50 ? 'text-amber-500' : 'text-red-500'}`}>{confidence}%</span>
          </div>
          <div className="w-full bg-slate-100 rounded-full h-2.5">
            <div className={`h-2.5 rounded-full transition-all ${confidence >= 70 ? 'bg-teal-500' : confidence >= 50 ? 'bg-amber-400' : 'bg-red-400'}`} style={{ width: `${confidence}%` }} />
          </div>
          <p className="text-xs text-slate-400 mt-2">
            Based on {input.symptoms.length} symptom{input.symptoms.length > 1 ? 's' : ''} provided. 
            {confidence < 70 && ' More symptoms or profile data would improve accuracy.'}
            {' '}Note: AI results are not medical advice.
          </p>
        </div>

        {/* Emergency */}
        {results.urgencyLevel === 'emergency' && (
          <div className="bg-red-100 border-2 border-red-500 rounded-lg p-5 mb-6">
            <div className="flex items-start gap-4">
              <AlertCircle className="w-8 h-8 text-red-600 flex-shrink-0" />
              <div className="flex-1">
                <h2 className="text-lg font-bold text-red-800 mb-2">Seek Immediate Medical Attention</h2>
                <p className="text-red-700 mb-4">Your symptoms require emergency care. Please do not wait.</p>
                <a href="tel:911" className="inline-flex items-center gap-2 bg-red-600 text-white px-6 py-3 rounded-lg font-semibold hover:bg-red-700 transition-colors"><Phone className="w-5 h-5" />Call 911</a>
              </div>
            </div>
          </div>
        )}

        {/* Urgency banner */}
        {results.urgencyLevel !== 'emergency' && (
          <div className={`${urgencyConfig.bgColor} ${urgencyConfig.borderColor} border rounded-lg p-5 mb-6`}>
            <div className="flex items-start gap-4">
              <div className={urgencyConfig.iconColor}>{urgencyConfig.icon}</div>
              <div>
                <h2 className={`text-lg font-semibold ${urgencyConfig.textColor} mb-1`}>{urgencyConfig.title}</h2>
                <p className="text-slate-600">{urgencyConfig.description}</p>
                {/* v2: Talk to Nurse Now for urgent — Roxette */}
                {results.urgencyLevel === 'urgent' && (
                  <a href="https://www.teladoc.com" target="_blank" rel="noopener noreferrer"
                    className="inline-flex items-center gap-2 mt-3 bg-orange-600 text-white px-4 py-2 rounded-lg text-sm font-semibold hover:bg-orange-500 transition-colors">
                    <Phone className="w-4 h-4" /> Talk to a Nurse Now <ExternalLink className="w-3 h-3" />
                  </a>
                )}
              </div>
            </div>
          </div>
        )}

        {/* v2: Explainability — "How this was determined" */}
        {results.explanationOfReasoning && (
          <div className="bg-white border border-slate-200 rounded-lg mb-5">
            <button onClick={() => setShowExplanation(!showExplanation)}
              className="w-full flex items-center justify-between p-5 text-left">
              <span className="font-semibold text-slate-800">🔍 How this was determined</span>
              {showExplanation ? <ChevronUp className="w-5 h-5 text-slate-400" /> : <ChevronDown className="w-5 h-5 text-slate-400" />}
            </button>
            {showExplanation && (
              <div className="px-5 pb-5 border-t border-slate-100">
                <p className="text-sm text-slate-600 mt-4 leading-relaxed">{results.explanationOfReasoning}</p>
                <p className="text-xs text-slate-400 mt-3">Model: GPT-4o · Analysis logged for accountability review</p>
              </div>
            )}
          </div>
        )}

        {/* Possible Causes */}
        <ResultCard title="What this might be">
          <ul className="space-y-3">
            {results.possibleCauses.map((cause, i) => (
              <li key={i} className="flex items-start gap-3 text-slate-600"><span className="text-slate-400 mt-1">•</span>{cause}</li>
            ))}
          </ul>
        </ResultCard>

        {/* Recommendations */}
        <ResultCard title="What you can do now">
          <ul className="space-y-3">
            {results.recommendations.map((rec, i) => (
              <li key={i} className="flex items-start gap-3 text-slate-600"><span className="text-lg">{getIcon(i)}</span>{rec}</li>
            ))}
          </ul>
        </ResultCard>

        {/* OTC with source tags — Roxette */}
        {results.otcSuggestions?.length > 0 && (
          <ResultCard title="OTC options to consider">
            <div className="space-y-4">
              {results.otcSuggestions.map((otc, i) => (
                <div key={i} className="bg-slate-50 border border-slate-200 rounded-lg p-4">
                  <div className="flex items-start justify-between gap-2 mb-1">
                    <h4 className="font-semibold text-slate-800">{otc.name}</h4>
                    {/* v2: Source tag */}
                    {otc.source && (
                      <span className="inline-flex items-center gap-1 bg-teal-100 text-teal-700 text-xs font-medium px-2 py-0.5 rounded-full flex-shrink-0">
                        ✓ {otc.source}
                      </span>
                    )}
                  </div>
                  <p className="text-xs text-slate-500 mb-2">{otc.brands?.join(', ')}</p>
                  <p className="text-sm text-slate-600">{otc.purpose}</p>
                  {/* v2: Contraindication warning — Roxette */}
                  {otc.contraindications && otc.contraindications.length > 0 && (
                    <div className="mt-3 bg-red-50 border border-red-200 rounded-lg p-3">
                      <p className="text-xs font-semibold text-red-700 mb-1">⚠️ Major Interaction Warning</p>
                      {otc.contraindications.map((c, j) => <p key={j} className="text-xs text-red-600">{c}</p>)}
                    </div>
                  )}
                  {otc.warnings && otc.warnings.length > 0 && (
                    <div className="mt-2 pt-2 border-t border-slate-200">
                      <p className="text-xs text-amber-600 flex items-start gap-1"><AlertTriangle className="w-3 h-3 mt-0.5 flex-shrink-0" />{otc.warnings[0]}</p>
                    </div>
                  )}
                </div>
              ))}
              <p className="text-xs text-slate-400">OTC suggestions cross-referenced against your health profile. Always read package instructions.</p>
            </div>
          </ResultCard>
        )}

        {/* Warning Signs */}
        {results.warningSignsToWatch?.length > 0 && (
          <div className="bg-amber-50 border border-amber-200 rounded-lg p-5 mb-5">
            <div className="flex items-start gap-3">
              <AlertTriangle className="w-6 h-6 text-amber-600 flex-shrink-0" />
              <div>
                <h3 className="font-semibold text-slate-800 mb-2">See a doctor if...</h3>
                <ul className="space-y-2">
                  {results.warningSignsToWatch.map((sign, i) => (
                    <li key={i} className="text-sm text-slate-600 flex items-start gap-2"><span className="text-amber-500">•</span>{sign}</li>
                  ))}
                </ul>
              </div>
            </div>
          </div>
        )}

        {/* v2: Human oversight banner */}
        <div className="bg-amber-50 border border-amber-200 rounded-lg p-4 mb-5">
          <p className="text-sm text-amber-800 text-center">⚕️ <strong>Human oversight required.</strong> Care360 AI is a triage navigator — not a replacement for a licensed healthcare provider. A doctor should make all final medical decisions.</p>
        </div>

        {/* Action Buttons */}
        <div className="grid sm:grid-cols-2 gap-4 mb-5">
          <Link href="/find-care?type=pharmacy" className="flex items-center justify-center gap-2 px-6 py-4 bg-white border border-slate-300 rounded-lg font-semibold text-slate-700 hover:bg-slate-50 transition-colors">
            <MapPin className="w-5 h-5" /> Find Pharmacy
          </Link>
          <Link href="/chat" className="flex items-center justify-center gap-2 px-6 py-4 bg-teal-600 rounded-lg font-semibold text-white hover:bg-teal-500 transition-colors">
            <MessageCircle className="w-5 h-5" /> Ask AI Advisor
          </Link>
        </div>

        {/* v2: Feedback widget — Alex/Max */}
        <div className="bg-white border border-slate-200 rounded-lg p-4 mb-5 flex items-center justify-between">
          <p className="text-sm text-slate-500">Help us improve</p>
          <FeedbackWidget page="results" />
        </div>

        {/* Disclaimer */}
        <div className="bg-slate-100 rounded-lg p-5 text-center">
          <p className="text-sm text-slate-500">⚠️ {results.disclaimers?.[0] || 'General wellness guidance only. Not medical advice. Always consult a healthcare provider.'}</p>
        </div>
        <div className="mt-6 text-center">
          <Link href="/symptoms" className="text-teal-600 font-medium hover:underline">← Check different symptoms</Link>
        </div>
      </div>
    </div>
  )
}

function ResultCard({ title, children }: { title: string; children: React.ReactNode }) {
  return (
    <div className="bg-white border border-slate-200 rounded-lg p-5 sm:p-6 mb-5">
      <h3 className="text-base font-semibold text-navy-950 mb-4">{title}</h3>
      {children}
    </div>
  )
}

function getUrgencyConfig(level: string) {
  switch (level) {
    case 'soon': return { icon: <AlertTriangle className="w-7 h-7" />, iconColor: 'text-amber-600', bgColor: 'bg-amber-50', borderColor: 'border-amber-200', textColor: 'text-amber-800', title: 'Consider seeing a doctor soon', description: 'Your symptoms may benefit from professional evaluation within the next few days.' }
    case 'urgent': return { icon: <AlertCircle className="w-7 h-7" />, iconColor: 'text-orange-600', bgColor: 'bg-orange-50', borderColor: 'border-orange-200', textColor: 'text-orange-800', title: 'Seek care today', description: 'Your symptoms suggest you should see a healthcare provider as soon as possible.' }
    default: return { icon: <CheckCircle className="w-7 h-7" />, iconColor: 'text-green-600', bgColor: 'bg-green-50', borderColor: 'border-green-200', textColor: 'text-green-800', title: 'Self-care likely appropriate', description: 'Based on your symptoms, rest and OTC remedies may help. Monitor for changes.' }
  }
}

function getIcon(i: number) { return ['💤','💧','🌡️','💊','🍵','🧘','🩺','📝'][i % 8] }
